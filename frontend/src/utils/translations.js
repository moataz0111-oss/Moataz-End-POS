// الحصول على اللغة الحالية
export const getCurrentLanguage = () => {
  return localStorage.getItem('app_language') || 'ar';
};

// تعيين اللغة
export const setLanguage = (lang) => {
  localStorage.setItem('app_language', lang);
  // تحديث اتجاه الصفحة
  const isRTL = ['ar', 'ku', 'fa', 'he'].includes(lang);
  document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
};

// دالة الترجمة الرئيسية
export const t = (key, fallback = null) => {
  const lang = getCurrentLanguage();
  const langTranslations = translations[lang] || translations.ar;
  return langTranslations[key] || translations.ar[key] || fallback || key;
};

// دالة للحصول على كل الترجمات للغة معينة
export const getTranslations = (lang = null) => {
  const currentLang = lang || getCurrentLanguage();
  return translations[currentLang] || translations.ar;
};

// دالة للتحقق من وجود ترجمة
export const hasTranslation = (key, lang = null) => {
  const currentLang = lang || getCurrentLanguage();
  const langTranslations = translations[currentLang] || {};
  return key in langTranslations;
};

// تصدير كل الترجمات
export default translations;
